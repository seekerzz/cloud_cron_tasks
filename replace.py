pattern = r'(<div class="paper-summary">).*?(</div>)'

count = 0
def replacer(match):
    global count
    if count < len(translations):
        res = f'{match.group(1)}\n                {translations[count]}\n            {match.group(2)}'
        count += 1
        return res
    return match.group(0)

